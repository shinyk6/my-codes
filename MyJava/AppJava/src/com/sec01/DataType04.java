package com.sec01;

public class DataType04 {
	public static void main(String[] args) {
		byte b = 100; //-128~127
		short s = 30000; 
		int i = 2000000;
		long l = 200000000000L; //0 10개부터는 long으로 선언 
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
	}
}



